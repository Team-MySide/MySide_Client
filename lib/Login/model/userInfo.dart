class UserInfo {
  String email;
  String password;
  String name;
  String phone;
  String relationNm;
  String nickname;
  String cancerNm;
  String stageNm;
  String progressNm;
  String gender;
  String age;
  String height;
  String weight;
  String diableFood;
  String disease;

  String get getEmail => this.email;

  set setEmail(String email) => this.email = email;

  get getPassword => this.password;

  set setPassword(password) => this.password = password;

  get getName => this.name;

  set setName(name) => this.name = name;

  get getPhone => this.phone;

  set setPhone(phone) => this.phone = phone;

  get getRelationNm => this.relationNm;

  set setRelationNm(relationNm) => this.relationNm = relationNm;

  get getNickname => this.nickname;

  set setNickname(nickname) => this.nickname = nickname;

  get getCancerNm => this.cancerNm;

  set setCancerNm(cancerNm) => this.cancerNm = cancerNm;

  get getStageNm => this.stageNm;

  set setStageNm(stageNm) => this.stageNm = stageNm;

  get getProgressNm => this.progressNm;

  set setProgressNm(progressNm) => this.progressNm = progressNm;

  get getGender => this.gender;

  set setGender(gender) => this.gender = gender;

  get getAge => this.age;

  set setAge(age) => this.age = age;

  get getHeight => this.height;

  set setHeight(height) => this.height = height;

  get getWeight => this.weight;

  set setWeight(weight) => this.weight = weight;

  get getDiableFood => this.diableFood;

  set setDiableFood(diableFood) => this.diableFood = diableFood;

  get getDisease => this.disease;

  set setDisease(disease) => this.disease = disease;
}
